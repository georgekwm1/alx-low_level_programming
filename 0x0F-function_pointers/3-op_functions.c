#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"


/**
 * op_add - summation of 2 numbers
 * @a: 1st argument
 * @b: 2nd argument
 *
 * Return: Integer.
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - difference of 2 numbers
 * @a: 1st argument
 * @b: 2nd argument
 *
 * Return: Nothing.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of 2 numbers
 * @a: 1st argument
 * @b: 2nd argument
 *
 * Return: Nothing.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division between 2 numbers
 * @a: 1st argument
 * @b: 2nd argument
 *
 * Return: Nothing.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus
 * @a: 1st argument
 * @b: 2nd argument
 *
 * Return: Nothing.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

